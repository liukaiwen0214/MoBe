/**
 * app.js
 * 文件用途：微信小程序的入口文件，初始化小程序实例
 * 所属模块：life-os-wechat（微信小程序模块）
 * 核心职责：
 * 1. 定义全局数据
 * 2. 处理小程序的生命周期事件
 * 3. 初始化用户信息
 * 与其他模块的关联：被所有页面和组件使用，提供全局数据和方法
 * 在整体业务流程中的位置：位于微信小程序的最顶层，是应用的入口点
 * 说明：使用App()函数注册小程序实例
 */

// app.js
App({
  /**
   * 全局数据
   * 存储小程序的全局数据，如用户信息等
   */
  globalData: {
    /**
     * 用户信息
     * 存储当前登录用户的信息，初始值为null
     */
    userInfo: null
  },

  /**
   * 小程序启动时执行的生命周期函数
   * 功能：初始化小程序，加载用户信息
   * 调用时机：小程序首次启动时
   * 核心流程：
   * 1. 从本地存储中获取用户信息
   * 2. 如果存在用户信息，将其设置到全局数据中
   */
  onLaunch() {
    // 从本地存储中获取用户信息
    const userInfo = wx.getStorageSync('userInfo')
    // 如果存在用户信息，将其设置到全局数据中
    if (userInfo) {
      this.globalData.userInfo = userInfo
    }
  }
})